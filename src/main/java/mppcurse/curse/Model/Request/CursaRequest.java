package mppcurse.curse.Model.Request;

public class CursaRequest {
    private int id;
    private int cap;
    private int nrPart;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCap() {
        return cap;
    }

    public void setCap(int cap) {
        this.cap = cap;
    }

    public int getNrPart() {
        return nrPart;
    }

    public void setNrPart(int nrPart) {
        this.nrPart = nrPart;
    }
}
